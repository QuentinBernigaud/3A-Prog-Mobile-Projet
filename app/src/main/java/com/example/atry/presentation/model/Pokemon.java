package com.example.atry.presentation.model;

public class Pokemon {

    private String url;
    private String name;
    private String weight;
    private String height;
    private String id;


    public String getUrl() {
        return url;
    }

    public String getName() {
        return name;
    }

    public String getWeight() {
        return weight;
    }

    public String getHeight() {
        return height;
    }

    public String getId() {
        return id;
    }
}
